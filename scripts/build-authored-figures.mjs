#!/usr/bin/env node
/**
 * Build `web/src/lib/figures/dessinScientifique.ts` from the hand-authored SVGs.
 *
 * Input:  a directory of `figNN_pP.svg` + `figures.json` (the authors' captions/omissions), and
 *         `manifest.json`, which maps each figure number back to the page and BLOCK it replaces.
 * Output: one generated TS module, keyed `page:blockId`, which the app applies to the book.
 *
 *   node scripts/build-authored-figures.mjs <dir-with-authored-svgs-and-manifest>
 *
 * The key is what makes this safe: a figure is bound to the exact OCR block it was drawn from, so
 * it can never be painted onto some other page. Anything in figures.json without a matching block
 * in the manifest is reported and skipped, never guessed into place.
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';

const dir = process.argv[2];
if (!dir) {
  console.error('usage: build-authored-figures.mjs <dir>');
  process.exit(1);
}

const manifest = JSON.parse(readFileSync(path.join(dir, 'manifest.json'), 'utf8'));
const meta = JSON.parse(readFileSync(path.join(dir, 'figures.json'), 'utf8'));
const byN = new Map(meta.map((m) => [m.n, m]));

/** Model-free, but still injected into the page and into exported HTML — so strip the same things. */
function sanitize(svg) {
  const i = svg.indexOf('<svg');
  const j = svg.lastIndexOf('</svg>');
  if (i === -1 || j === -1) throw new Error('not an svg');
  return svg
    .slice(i, j + 6)
    .replace(/<script[\s\S]*?<\/script>/gi, '')
    .replace(/<foreignObject[\s\S]*?<\/foreignObject>/gi, '')
    .replace(/\son\w+\s*=\s*("[^"]*"|'[^']*')/gi, '')
    .replace(/(href|xlink:href)\s*=\s*("(?!#)[^"]*"|'(?!#)[^']*')/gi, '')
    .replace(/<!--[\s\S]*?-->/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

const entries = [];
const missing = [];
for (const m of manifest) {
  const svgPath = path.join(dir, 'authored', m.file.replace(/\.png$/, '.svg'));
  if (!existsSync(svgPath)) {
    missing.push(`fig${String(m.n).padStart(2, '0')} (p.${m.pageHuman}) — no SVG`);
    continue;
  }
  const info = byN.get(m.n) ?? {};
  entries.push({
    key: `${m.page}:${m.blockId}`,
    svg: sanitize(readFileSync(svgPath, 'utf8')),
    caption: info.caption ?? '',
    omissions: (info.omissions ?? []).filter((s) => s && !/^aucune?$/i.test(s)),
    n: m.n,
    page: m.pageHuman,
  });
}

const body = entries
  .map(
    (e) =>
      `  // fig ${e.n} — p.${e.page}\n` +
      `  ${JSON.stringify(e.key)}: {\n` +
      `    svg: ${JSON.stringify(e.svg)},\n` +
      `    caption: ${JSON.stringify(e.caption)},\n` +
      (e.omissions.length ? `    omissions: ${JSON.stringify(e.omissions)},\n` : '') +
      `  },`,
  )
  .join('\n');

const out = `import type { FigureSet } from '../authoredFigures';

/**
 * GENERATED by scripts/build-authored-figures.mjs — do not edit by hand.
 *
 * "Exercices de géométrie descriptive", Muselu wa Muswiyi (Collection Boboto, 1988) — 51 pages,
 * ${entries.length} figures redrawn from the scan.
 *
 * Each SVG was drawn with every point MEASURED off a coordinate grid laid over the scan, and
 * checked against a potrace of the page's own ink — because in an épure the point positions are
 * the content, and a confident wrong point is indistinguishable from a right one. They remain
 * reconstructions: the UI shows each beside the original crop and badges it as redrawn, and any
 * label that could not be read is listed in \`omissions\` rather than guessed.
 *
 * Keyed \`page:blockId\`, so a figure can only ever land on the block it was drawn from.
 */
export const DESSIN_SCIENTIFIQUE: FigureSet = {
${body}
};
`;

const dest = path.resolve('web/src/lib/figures/dessinScientifique.ts');
writeFileSync(dest, out);
console.log(`wrote ${entries.length} figures -> ${dest}`);
if (missing.length) {
  console.warn(`\nMISSING (${missing.length}) — these figures have no drawing and will fall back to the scan:`);
  missing.forEach((m) => console.warn('  ' + m));
}
